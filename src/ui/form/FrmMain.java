    private void btnPosaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPosaljiActionPerformed
        String tekst = txtPoruka.getText().trim();
        if (tekst.isEmpty()) return;
        
        if (tekst.length() > 200) {
            JOptionPane.showMessageDialog(this, "Poruka je predugacka");
            return;
        }

        // ISPRAVLJENO: Logika za određivanje primaoca
        Object selected = cbKorisnici.getSelectedItem();
        User primalac = null;
        Operation op = Operation.SEND_MESSAGE_ALL;

        if (selected instanceof User) {
            primalac = (User) selected;
            op = Operation.SEND_MESSAGE_PRIVATE;
        } else if (selected instanceof String && !selected.equals("SVI")) {
            // Ako ti je ComboBox pun Stringova, ovde moraš naći User objekat
            // ali je najbolje da cbKorisnici prima objekte
        }

        Message msg = new Message(currentUser, primalac, tekst, new Date());

        try {
            Communication.getInstance().getSender().send(new Request(op, msg));
            txtPoruka.setText(""); // Očisti polje nakon slanja
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btnPosaljiActionPerformed

    private void tblPoslednje3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPoslednje3MouseClicked
        int row = tblPoslednje3.getSelectedRow();
        if(row != -1){
            MessageTableModel model = (MessageTableModel) tblPoslednje3.getModel();
            Message m = model.getMessageAt(row);
            JOptionPane.showMessageDialog(null, m.getText(), "Cela poruka", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_tblPoslednje3MouseClicked

