    private void prepareView(){
        try {
            // zahtev serveru da nam posalje ko je sve online
            Request req = new Request(Operation.GET_LOGGED_USERS, null);
            Communication.getInstance().getSender().send(req);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    // Ovu metodu poziva ReceiverThread kada nesto stigne sa servera
    public void handleResponse(Response response){
       if (response.getResult() instanceof List) {
            List<User> ulogovani = (List<User>) response.getResult();
            cbKorisnici.removeAllItems();
            cbKorisnici.addItem("SVI"); // Ovde "SVI" ostaje String, zato je cbKorisnici<Object> ili <String>
            for (User u : ulogovani) {
                if (!u.equals(currentUser)) {
                    cbKorisnici.addItem(u.toString()); // Ili samo u, ako je combo box tipa User
                }
            }
        } else if (response.getResult() instanceof Message) {
            // ISPRAVLJENO: Dodavanje poruke u listu i osvežavanje
            Message m = (Message) response.getResult();
            allReceivedMessages.add(m);
            refreshMessageTables();
        }
    }
    
    private void refreshMessageTables() {
    List<Message> last3 = new ArrayList<>();
    List<Message> others = new ArrayList<>();

    int total = allReceivedMessages.size();
    
    if (total <= 3) {
        last3.addAll(allReceivedMessages);
    } else {
        // Poslednje 3 su na kraju liste
        last3 = allReceivedMessages.subList(total - 3, total);
        // Sve ostale su od početka do total - 3
        others = allReceivedMessages.subList(0, total - 3);
    }

    tblPoslednje3.setModel(new MessageTableModel(last3));
    tblOstale.setModel(new MessageTableModel(others));
}

